package MP9;

public class Spade {
    private String Spade;
    private String Rank;
    
    public Spade(String spade,String rank){
        this.Spade = spade;
        this.Rank = rank;
    }
    public String getSpade(){
        return Spade;
    }
    public String getRank(){
        return Rank;
    }
}
